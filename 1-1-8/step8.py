num = int(input("Enter a Grade -> "))
if (num >= 90):
  print("The Grade is an A or higher")
elif (num >= 80):
  print("The Grade is an B or higher")
elif (num >= 70):
  print("The Grade is an C or higher")
elif (num >= 60):
  print("The Grade is an F or higher")
else:
  print("The Grade is an F or lower")
